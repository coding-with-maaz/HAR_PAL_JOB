<script>
    document.getElementById('role').addEventListener('change', function() {
        const companyInfo = document.getElementById('company-info');
        if (this.value === 'employer') {
            companyInfo.classList.remove('hidden');
            // Make company fields required
            document.querySelectorAll('#company-info input, #company-info textarea, #company-info select').forEach(element => {
                element.required = true;
            });
        } else {
            companyInfo.classList.add('hidden');
            // Remove required attribute from company fields
            document.querySelectorAll('#company-info input, #company-info textarea, #company-info select').forEach(element => {
                element.required = false;
            });
        }
    });
</script>
